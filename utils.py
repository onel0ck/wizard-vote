def process_submission_input(submission_input: str) -> str:
    if submission_input.isdigit():
        return f"https://bitcoinwizards.vercel.app/submissions/{submission_input}"
    if submission_input.startswith("https://bitcoinwizards.vercel.app/submissions/"):
        return submission_input
    raise ValueError(f"Invalid submission format: {submission_input}")

def read_submissions() -> List[str]:
    with open(SUBMISSIONS_FILE, 'r') as f:
        lines = [line.strip() for line in f if line.strip()]
        return [process_submission_input(line) for line in lines]

def read_proxies() -> List[str]:
    with open(PROXIES_FILE, 'r') as file:
        return [line.strip() for line in file if line.strip()]

class VotingSession:
    def __init__(self, proxy: str, submission_urls: List[str]):
        self.proxy = proxy
        self.submission_urls = submission_urls
        self.user_agent = random.choice(USER_AGENTS)
        self.csrf_token = None
        self.csrf_secret = None
        
    def get_headers(self, additional_headers: Dict = None) -> Dict:
        headers = {
            "accept": "application/json, text/plain, */*",
            "accept-encoding": "gzip, deflate, br, zstd",
            "accept-language": "en-US,en;q=0.9",
            "content-type": "application/json",
            "origin": BASE_URL,
            "referer": f"{BASE_URL}/submissions/",
            "sec-ch-ua-mobile": "?0",
            "sec-ch-ua-platform": "Windows",
            "sec-fetch-dest": "empty",
            "sec-fetch-mode": "cors",
            "sec-fetch-site": "same-origin",
            "user-agent": self.user_agent
        }
        
        if self.csrf_token:
            headers["x-csrf-token"] = self.csrf_token
        if additional_headers:
            headers.update(additional_headers)
        return headers

    @staticmethod
    def generate_fingerprint() -> str:
        random_str = f"{random.random()}{datetime.now().timestamp()}"
        return hashlib.md5(random_str.encode()).hexdigest()

    async def initialize_session(self, session: aiohttp.ClientSession) -> bool:
        try:
            async with session.get(CSRF_ENDPOINT, headers=self.get_headers(), proxy=self.proxy) as response:
                if response.status != 200:
                    return False
                
                data = await response.json()
                self.csrf_token = data.get("csrfToken")
                self.csrf_secret = response.cookies.get("csrf_secret")
                return bool(self.csrf_token and self.csrf_secret)
        except Exception as e:
            print(f"Error initializing session: {e}")
            return False

    async def vote_for_submission(self, session: aiohttp.ClientSession, submission_url: str) -> bool:
        try:
            submission_id = submission_url.split("/")[-1]
            
            async with session.get(
                f"{SUBMISSION_ENDPOINT}/{submission_id}", 
                headers=self.get_headers(),
                proxy=self.proxy
            ) as response:
                if response.status != 200:
                    return False
                    
                submission_data = await response.json()
                attachment_id = submission_data["submission"]["attachments"][0]["id"]
            
            await asyncio.sleep(random.uniform(MIN_DELAY, MAX_DELAY))
            
            rating = random.choice(RATINGS)
            payload = {
                "submissionId": int(submission_id),
                "attachmentId": attachment_id,
                "value": rating,
                "fingerprint": self.generate_fingerprint()
            }
            
            vote_headers = self.get_headers({
                "content-type": "application/json",
                "origin": BASE_URL,
                "referer": f"{BASE_URL}/submissions/{submission_id}"
            })
            
            async with session.post(
                RATE_ENDPOINT,
                headers=vote_headers,
                json=payload,
                proxy=self.proxy
            ) as response:
                success = response.status == 200
                if success:
                    print(f"Successfully voted {rating} stars for submission {submission_id}")
                return success
                
        except Exception as e:
            print(f"Error voting for submission: {e}")
            return False